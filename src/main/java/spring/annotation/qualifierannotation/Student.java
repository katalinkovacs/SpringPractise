package spring.annotation.qualifierannotation;


public class Student {

    private int age;
    private String name;

    // GETTERS SETTERS
    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

}
